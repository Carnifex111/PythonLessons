my_list = [1, 2, 3, 4, 5]
for element in my_list:
    # выполняемый блок кода для каждого элемента списка
    print(element)

my_string = "Hello"
for char in my_string:
    # выполняемый блок кода для каждого символа в строке
    print(char)


for i in range(5):
    # выполняемый блок кода 5 раз (от 0 до 4)
    print(i)

i = 0
while i < 5:
    # выполняемый блок кода до тех пор, пока i меньше 5
    print(i)
    i += 1
